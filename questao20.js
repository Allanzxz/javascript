/*
Questão 20: Como você pode chamar uma função de retorno de chamada em
JavaScript?
*/
RESPOSTA:
function minhaFuncaoDeCallback(parametro1, parametro2) {
    // Corpo da função de callback
    console.log(parametro1 + parametro2);
}

// Chamar a função de retorno de chamada
minhaFuncaoDeCallback(10, 20); // Saída: 30
